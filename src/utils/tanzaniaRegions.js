// Get all region names
export const getRegionNames = () => {
  return tanzaniaRegions.map(region => region.name);
};

// Get all district names
export const getAllDistrictNames = () => {
  const allDistricts = [];
  tanzaniaRegions.forEach(region => {
    allDistricts.push(...region.districts);
  });
  return allDistricts;
};

// Get districts for a specific region
export const getDistrictNames = (regionName) => {
  const region = tanzaniaRegions.find(region => region.name === regionName);
  return region ? region.districts : [];
}; 